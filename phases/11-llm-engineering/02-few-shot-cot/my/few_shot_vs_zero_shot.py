import anthropic

client = anthropic.Anthropic()

tickets = [
    "С карты списали оплату дважды за один месяц",
    "Не могу загрузить отчёт, кнопка не нажимается",
    "Сколько стоит подключить ещё 5 рабочих мест?",
    "Надоело, хочу закрыть аккаунт и забрать деньги",
]
system_zero = (
    "Ты классификатор обращений. В ответе только название категории, без пояснений"
)
system_few = """Ты классификатор обращений. В ответе только название категории, без пояснений.
Примеры обращений и их классификации:
'почему-то вчера списали деньги еще раз, хотя заплатила в начале месяца'/ биллинг
'страница с основными отчетами не отображается, статус 404' / технический сбой
'Подскажите по стоимости перехода с базового тарифа на Pro' / продажи
'мне не подошел продукт, хочу забрать оставшиеся деньги'  / отмена"""


def classify(system, tiket):
    resp = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=200,
        temperature=0,
        system=system,
        messages=[{"role": "user", "content": tiket}],
    )
    return resp.content[0].text.strip()


for tiket in tickets:
    print(f"--- тикет: {tiket} ---")
    print(f"без примера: {classify(system_zero, tiket)}")
    print(f"c примером: {classify(system_few, tiket)}")
