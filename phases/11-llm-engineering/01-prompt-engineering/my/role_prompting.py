import anthropic

client = anthropic.Anthropic()
user_talk = """мне нужна сводка по отчету NPS. В нем есть оценка и текст отзыва. Проанализируй этот отчет. Я хочу знать:

1. распределение оценок по группам в процентах (1-3, 4-8, 9 и более)
2. саммари тектсов самых лучших оценок и самых худших

Формат ответа в виде xml:
<распределение>
    <9-10>
       <проценты></проценты>
       <текст>самммари текста</текст>
    </9-10>
     другие группы
</распределение>

НЕ использовать символы маркировки типа контента `xml` и т.п.
НЕ писать длинный самари. Максимум - 1 предложение

ЕСЛИ отзыв пустой/мусорный/английский — не анализируй текст, а только оценку
ЕСЛИ количество оценок в группе менее 2, написать "слишком мало оценок"

ВСЕГДА выводи все три группы, даже если в группе 0 оценок (проценты = 0).

Поставь валидные названия XML тегов.

Оценка: 2 | "Поддержка ужасная, отвечают сутками"
Оценка: 6 | "Нормально, но не хватает интеграции с телефонией"
Оценка: 7 | "ок, но дорого за это"
Оценка: 8 | ""
Оценка: 4 | "slow and buggy"

выдай чистый XML без markdown-разметки и без тройных кавычек."""

roles = [
    (
        "тест",
        """Я CMO CRM-сервиса""",
    ),
]

for label, system_prompt in roles:
    response = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=512,
        temperature=0.0,
        system=system_prompt,
        messages=[{"role": "user", "content": user_talk}],
    )

    print(f"--- {label} ---")
    for block in response.content:
        if block.type == "text":
            print(block.text)
    print()

